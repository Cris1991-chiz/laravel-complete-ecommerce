    <div class="col-description">
        <p>The Mavic from DJI packs features you once thought possible only on much larger platforms into a compact quadcopter that is snappy, agile, and captures high-resolution images. 
            The drone features an advanced flight control system that draws on a host of sensors — including a ground-facing camera, ultrasound, GPS, dual redundant IMUs, and more — to keep track of where it is flying in 3D space and even avoid collisions. 
            The Mavic works in tandem with DJI's GO mobile app for accessing settings, getting a telemetry readout, viewing a low-latency video feed, and even editing and sharing your footage. 
            In addition to traditional joystick style controls, you can fly with simple tap-based commands, and the Mavic can even recognize gestures for the perfect selfie.
        </p>
        <h4>Integrated Camera and Gimbal</h4>
        <h5>3-Axis Gimbal Stabilizer</h5>
        <p>Using three specially miniaturized brushless motors and advanced algorithms, the camera moves in the 
            opposite direction to the Mavic, cancelling out unwanted movement for the smoothest possible video.</p>
        <h5>DCI &amp; UHD 4K and Full HD Video</h5>
        <p>The Mavic Pro camera shoots 4K video (up to 4096 x 2160) at 30 frames per second and Full HD 1080p at 96 frames per second, so you can create incredible slow motion. 
            Its minimum focusing distance is just 19", making it perfect for everything from the ultimate aerial selfies to landscape shots.
        </p>
        <h5>ActiveTrack Subject Tracking Modes</h5>
        <p>With ActiveTrack, just tell the Mavic who to track and it handles the rest. No GPS bracelets or transmitters are required. The Mavic has been trained to detect and recognize a number of common subjects including people, bike riders, vehicles, and even animals. Once you have marked your subject, you can fly around them to create a huge variety of shots, depending on the mode you are in. As the Mavic is tracking, you can even select exactly where you want the subject in the frame.

            Three modes are available:
            
            Trace - Follow behind or in front of your subject, or circle it as it moves
            Profile - Fly alongside your subject
            Spotlight - Keep the camera trained on your subject while you fly almost anywhere
        </p>
